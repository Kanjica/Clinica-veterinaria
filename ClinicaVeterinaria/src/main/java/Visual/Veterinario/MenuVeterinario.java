    private void RegistrarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarConsultaActionPerformed
        // dentro do seu método de ação
         Window window = SwingUtilities.getWindowAncestor(this); // 'this' é o painel onde está o botão

         if (window != null) {
           window.dispose(); // fecha a janela que contém esse painel
          }
         
        JFrame frame = new JFrame("Registrar Consulta");
        frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        frame.setSize(800, 600);// ajusta tamanho da janela ao tamanho do painel e componentes
        frame.setLocationRelativeTo(null); // centraliza

        RegistraConsulta painel = new RegistraConsulta(petShop, veterinario);
        frame.add(painel);

        frame.setVisible(true);
        
    }//GEN-LAST:event_RegistrarConsultaActionPerformed

